/**
 * Get authorization headers with JWT token
 */
async function getAuthHeaders(): Promise<HeadersInit> {
    const headers: HeadersInit = {
        'Content-Type': 'application/json',
    };

    // Get fresh Firebase ID token using token manager (same as other services)
    try {
        const { firebaseTokenManager } = await import('./firebaseTokenManager');

        // Retry logic: Try up to 3 times with delay if token is null (Firebase might be initializing)
        let token: string | null = null;
        let attempts = 0;
        const maxAttempts = 3;

        while (!token && attempts < maxAttempts) {
            token = await firebaseTokenManager.getValidToken();

            if (!token && attempts < maxAttempts - 1) {
                // Wait 500ms before retry
                await new Promise(resolve => setTimeout(resolve, 500));
                logger.info(`🔄 Retrying token fetch (attempt ${attempts + 2}/${maxAttempts})`);
            }
            attempts++;
        }

        if (token) {
            headers['Authorization'] = `Bearer ${token}`;
            logger.dev('🔑 Auth token added to headers');
        } else {
            logger.warn('⚠️ No auth token available from firebaseTokenManager after retries');
        }
    } catch (error) {
        logger.error('❌ Error getting auth token:', error);
    }

    return headers;
}

/**
 * Get optional authorization headers (for public endpoints that work with or without auth)
 */
async function getOptionalAuthHeaders(): Promise<HeadersInit> {
    const headers: HeadersInit = {
        'Content-Type': 'application/json',
    };

    // Try to get token, but don't fail if not available
    try {
        const { firebaseTokenManager } = await import('./firebaseTokenManager');
        const token = await firebaseTokenManager.getValidToken();

        if (token) {
            headers['Authorization'] = `Bearer ${token}`;
            logger.dev('🔑 Optional auth token added to headers');
        } else {
            logger.info('ℹ️ No auth token available, proceeding without authentication');
        }
    } catch (error) {
        logger.info('ℹ️ Auth token not available, proceeding without authentication');
    }

    return headers;
}

/**
 * Handle API response errors
 */
async function handleResponse<T>(response: Response): Promise<T> {
    if (!response.ok) {
        const errorData = await response.json().catch(() => ({}));
        const errorMessage = errorData.detail || errorData.message || `HTTP ${response.status}: ${response.statusText}`;

        logger.error('❌ API Error:', {
            status: response.status,
            statusText: response.statusText,
            error: errorData
        });

        throw new Error(errorMessage);
    }

    return response.json();
}

