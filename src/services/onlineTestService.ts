/**
 * Get test by ID (for taking test - no answers included)
 */
export async function getTest(testId: string): Promise<Test> {
    try {
        logger.info('📖 Fetching test:', testId);

        const headers = await getAuthHeaders();
        const response = await fetch(`${API_BASE_URL}/api/v1/tests/${testId}`, {
            method: 'GET',
            headers,
        });

        const result = await handleResponse<Test>(response);

        // DEBUG: RAW API RESPONSE


        // DEBUG: Log evaluation_criteria for diagnostic tests
        if (result.test_category === 'diagnostic') {
            logger.info('🔍 Diagnostic test loaded (getTest):', {
                test_id: testId,
                test_category: result.test_category,
                has_evaluation_criteria: !!result.evaluation_criteria,
                evaluation_criteria_length: result.evaluation_criteria?.length
            });
        }

        //  NORMALIZE: Map option_key/option_text to key/text for frontend compatibility
        if (result.questions) {
            result.questions = result.questions.map(q => {
                const normalized: any = {
                    ...q,
                    options: (q.options || []).map(opt => ({
                        key: opt.option_key || opt.key || '',
                        text: opt.option_text || opt.text || '',
                        option_key: opt.option_key || opt.key || '',
                        option_text: opt.option_text || opt.text || ''
                    })),
                    correct_answer_key: q.correct_answer_key || (q.correct_answer_keys && q.correct_answer_keys[0]) || '',
                    max_points: q.max_points || 1
                };

                // NORMALIZE: Ensure correct_answers field exists (backward compatibility)
                // Backend may send correct_answers OR legacy fields, handle both

                // MCQ: Ensure correct_answers is array
                if (q.question_type === 'mcq' || q.question_type === 'mcq_multiple') {
                    if (!q.correct_answers || !Array.isArray(q.correct_answers) || q.correct_answers.length === 0) {
                        // Fallback to legacy fields
                        if (q.correct_answer_keys && q.correct_answer_keys.length > 0) {
                            normalized.correct_answers = q.correct_answer_keys;
                        } else if (q.correct_answer_key) {
                            normalized.correct_answers = [q.correct_answer_key];
                        }
                    } else {
                        // Backend sent correct_answers, keep it
                        normalized.correct_answers = q.correct_answers;
                    }
                }

                // Matching: Keep correct_answers as array for display, but also create object version
                if (q.question_type === 'matching') {
                    // Convert correct_matches from array to object for backward compatibility
                    if (Array.isArray(q.correct_matches)) {
                        normalized.correct_matches = (() => {
                            const matchesObj: Record<string, string> = {};
                            (q.correct_matches as any[]).forEach((match: any) => {
                                if (match.left_key && match.right_key) {
                                    matchesObj[match.left_key] = match.right_key;
                                }
                            });
                            return matchesObj;
                        })();
                    }
                    // Ensure correct_answers exists (backend should send this)
                    if (q.correct_answers && Array.isArray(q.correct_answers)) {
                        normalized.correct_answers = q.correct_answers;
                    }
                }

                // Map Labeling: Keep correct_answers as array for display
                if (q.question_type === 'map_labeling') {
                    if (q.correct_answers && Array.isArray(q.correct_answers)) {
                        normalized.correct_answers = q.correct_answers;
                    }
                }

                // Completion: Keep correct_answers as array for display
                if (q.question_type === 'completion') {
                    if (q.correct_answers && Array.isArray(q.correct_answers)) {
                        normalized.correct_answers = q.correct_answers;
                    }
                }

                // Sentence Completion & Short Answer: Keep correct_answers as-is
                // These types have correct_answers inside sentences[]/questions[] arrays
                if (q.question_type === 'sentence_completion' || q.question_type === 'short_answer') {
                    // Backend handles these at sub-question level
                    // No normalization needed
                }

                return normalized;
            });

            // INFER test_format if missing (for backward compatibility)
            if (!result.test_format && result.questions.length > 0) {
                const hasEssay = result.questions.some(q => q.question_type === 'essay');
                const hasMcq = result.questions.some(q => q.question_type === 'mcq' || !q.question_type);

                if (hasEssay && hasMcq) {
                    result.test_format = 'mixed';
                } else if (hasEssay) {
                    result.test_format = 'essay';
                } else {
                    result.test_format = 'mcq';
                }
                logger.info('ℹ️ Inferred test format:', result.test_format);
            }
        }

        return result;
    } catch (error) {
        logger.error('❌ Failed to fetch test:', error);
        throw error;
    }
}

