// UNIFIED FIELD TYPES (API v2.3 Migration - December 2025)
export type MatchPair = { left_key: string; right_key: string };
export type MapLabelAnswer = { label_key: string; option_key: string };
export type CompletionAnswer = { blank_key: string; answers: string[] }; // [{blank_key: "1", answers: ["text"]}]

export interface TestQuestion {
    question_id: string;
    question_text: string;

    // NEW: 8 Question Types (December 9, 2025 - IELTS support)
    // UPDATED: December 12, 2025 - Added 'mcq_multiple' for multi-answer MCQ questions
    // UPDATED: December 15, 2025 - Added 'true_false_multiple' for Vietnamese exam format
    question_type?: 'mcq' | 'mcq_multiple' | 'essay' | 'matching' | 'map_labeling' | 'completion' | 'sentence_completion' | 'short_answer' | 'true_false_multiple';
    question_number?: number; // Question number in sequence
    max_points?: number; // Points for this question (default: 1 for MCQ, customizable for others)
    instruction?: string; // Instructions specific to this question

    // Grading/feedback fields (owner view only)
    grading_rubric?: string; // Grading guidance for essay questions
    explanation?: string; // Explanation for correct answer (hidden from students)
    sample_answer?: string; // Model answer for essay (hidden from students)

    // MCQ-specific fields (question_type === 'mcq')
    options?: {
        key?: string; // LEGACY: A, B, C, D, E...
        text?: string; // LEGACY
        option_key: string; // NEW: Backend uses this
        option_text: string; // NEW: Backend uses this
    }[];
    correct_answer_key?: string; // LEGACY: Single answer (owner view only)
    correct_answer_keys?: string[]; // LEGACY: Multi-answer support (owner view only)

    // Matching-specific fields (question_type === 'matching')
    left_items?: { key: string; text: string }[]; // Items to match (e.g., [{"key": "1", "text": "John"}])
    right_options?: { key: string; text: string }[]; // Available options (e.g., [{"key": "A", "text": "Paris"}])
    correct_matches?: Record<string, string>; // LEGACY: Owner view only: {"1": "A", "2": "B"}

    // Map Labeling-specific fields (question_type === 'map_labeling')
    diagram_url?: string; // URL to diagram/map image
    diagram_description?: string; // Alt text for diagram
    label_positions?: { key: string; description?: string; x?: number; y?: number }[]; // Label positions
    correct_labels?: Record<string, string>; // LEGACY: Owner view only: {"1": "C", "2": "D"}

    // UNIFIED FIELD (API v2.3 - December 2025)
    // Replaces: correct_answer_keys, correct_matches, correct_labels, and completion correct_answers
    // Type varies by question_type:
    // - MCQ: string[] (e.g., ["A", "B"])
    // - Matching: MatchPair[] (e.g., [{left_key: "1", right_key: "A"}])
    // - Map Labeling: MapLabelAnswer[] (e.g., [{label_key: "1", option_key: "C"}])
    // - Completion: CompletionAnswer[] (e.g., [{blank_key: "1", answers: ["text"]}])
    correct_answers?: string[] | MatchPair[] | MapLabelAnswer[] | CompletionAnswer[];

    // Completion-specific fields (question_type === 'completion')
    template?: string; // Template with blanks: "Flight: [1]_____ Gate: [2]_____"
    blanks?: { key: string; position: string; word_limit?: number }[]; // Blank definitions
    case_sensitive?: boolean; // Default: false

    // Sentence Completion-specific (question_type === 'sentence_completion')
    sentences?: {
        key: string;
        template: string; // "The customer wants to book a flight to _____."
        word_limit?: number;
        correct_answers?: string[]; // Owner view only: ["Paris", "Paris France"]
    }[];

    // Short Answer-specific (question_type === 'short_answer')
    questions?: {
        key: string;
        text: string; // "What is the customer's last name?"
        word_limit?: number;
        correct_answers?: string[]; // Owner view only: ["Smith", "SMITH"]
    }[];

    // True/False Multiple-specific (question_type === 'true_false_multiple')
    // Vietnamese exam format: Multiple true/false statements per question
    statements?: {
        key: string; // "a", "b", "c", "d"
        text: string; // Statement text
        correct_value?: boolean; // Owner view only: true/false
    }[];
    scoring_mode?: 'partial' | 'all_or_nothing'; // Partial: proportional scoring, All-or-nothing: full points or 0
    points?: number; // 1-5 points per question

    // NEW: Listening test fields (December 8, 2025)
    audio_section?: number; // Which audio section this question refers to (1-based)
    timestamp_hint?: string; // Time range hint (e.g., "0:15-0:20")

    // NEW: Media support (November 12, 2025)
    media_type?: 'image' | 'audio';
    media_url?: string; // R2 public URL
    media_description?: string; // Alt text or caption
}

