/**
 * Submit test answers
 * Support both MCQ and Essay answers
 */
export async function submitTest(
    testId: string,
    sessionId: string,
    questions: TestQuestion[], // ✅ NEW: All questions to ensure we send all answers
    answers: Record<string, string>,
    essayAnswers?: Record<string, { essay_answer: string; media_attachments?: AnswerMediaAttachment[] }>,
    matchingAnswers?: Record<string, Record<string, string>>,
    mapLabelingAnswers?: Record<string, Record<string, string>>,
    completionAnswers?: Record<string, Record<string, string>>,
    sentenceCompletionAnswers?: Record<string, Record<string, string>>,
    shortAnswerAnswers?: Record<string, Record<string, string>>,
    trueFalseAnswers?: Record<string, Record<string, boolean>>
): Promise<TestSubmission> {
    try {
        logger.info('📤 Submitting test:', testId, sessionId);
        logger.info('   Total questions:', questions.length);

        // Build user_answers array - ITERATE OVER ALL QUESTIONS
        const userAnswers = questions.map(q => {
            const qid = q.question_id;
            const qtype = q.question_type || 'mcq'; // Default to mcq if type missing

            switch (qtype) {
                case 'mcq':
                case 'mcq_multiple':
                    return {
                        question_id: qid,
                        question_type: qtype,
                        selected_answer_keys: answers[qid] ? (Array.isArray(answers[qid]) ? answers[qid] : [answers[qid]]) : []
                    };

                case 'essay':
                    const essayData = essayAnswers?.[qid];
                    return {
                        question_id: qid,
                        question_type: 'essay',
                        essay_answer: essayData?.essay_answer || '',
                        media_attachments: essayData?.media_attachments || []
                    };

                case 'matching':
                    return {
                        question_id: qid,
                        question_type: 'matching',
                        matches: matchingAnswers?.[qid] || {} // Empty object if no matches
                    };

                case 'map_labeling':
                    return {
                        question_id: qid,
                        question_type: 'map_labeling',
                        labels: mapLabelingAnswers?.[qid] || {} // Empty object if no labels
                    };

                case 'completion':
                    return {
                        question_id: qid,
                        question_type: 'completion',
                        answers: completionAnswers?.[qid] || {} // Empty object if no answers
                    };

                case 'sentence_completion':
                    return {
                        question_id: qid,
                        question_type: 'sentence_completion',
                        answers: sentenceCompletionAnswers?.[qid] || {} // Empty object if no answers
                    };

                case 'short_answer':
                    return {
                        question_id: qid,
                        question_type: 'short_answer',
                        answers: shortAnswerAnswers?.[qid] || {} // Empty object if no answers
                    };

                case 'true_false_multiple':
                    return {
                        question_id: qid,
                        question_type: 'true_false_multiple',
                        user_answer: trueFalseAnswers?.[qid] || {} // Empty object if no answers
                    };

                default:
                    // Fallback to MCQ for unknown types
                    return {
                        question_id: qid,
                        question_type: 'mcq',
                        selected_answer_keys: []
                    };
            }
        });

        logger.info('📋 Built user_answers:', userAnswers.length, 'questions');
        userAnswers.forEach((ua, i) => {
            logger.info(`   ${i + 1}. ${ua.question_id} (${ua.question_type})`);
        });

        const headers = await getAuthHeaders();
        const response = await fetch(`${API_BASE_URL}/api/v1/tests/${testId}/submit`, {
            method: 'POST',
            headers,
            body: JSON.stringify({
                session_id: sessionId,
                user_answers: userAnswers,
            }),
        });

        const result = await handleResponse<TestSubmission>(response);
        logger.info('✅ Test submitted successfully. Score:', result.score);
        return result;
    } catch (error) {
        logger.error('❌ Failed to submit test:', error);
        throw error;
    }
}

