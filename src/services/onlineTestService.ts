/**
 * Merge multiple tests into one new test
 * POST /api/v1/tests/merge
 * Dec 11, 2025 - Test Merge Feature
 */
export interface MergeTestsRequest {
    source_test_ids: string[]; // 2-10 test IDs
    title: string; // 5-200 chars
    description?: string; // max 1000 chars
    creator_name?: string; // 2-100 chars
    test_type: 'mcq' | 'essay' | 'mixed' | 'listening' | 'auto';
    test_category?: 'academic' | 'diagnostic'; // default: academic
    time_limit_minutes: number; // 1-300, default: 30
    max_retries?: number; // 1-10, default: 3
    passing_score?: number; // 0-100, default: 50
    mcq_points?: number; // for mixed type
    essay_points?: number; // for mixed type
    essay_grading_criteria?: string; // max 2000 chars
    question_selection: 'all' | 'random' | 'custom'; // default: all
    max_questions?: number; // required if question_selection=random
    custom_selection?: Record<string, {
        question_indices: number[];
        part_title?: string;
        part_description?: string;
    }>; // required if question_selection=custom
    deadline?: string; // ISO datetime
    show_answers_timing?: 'immediate' | 'after_deadline'; // default: immediate
}

export interface MergeTestsResponse {
    success: boolean;
    test_id: string;
    title: string;
    test_type: string;
    num_questions: number;
    num_mcq_questions: number;
    num_essay_questions: number;
    max_points: number;
    source_tests: number;
    created_at: string;
    message: string;
    parts?: Array<{
        part_number: number;
        part_title?: string;
        part_description?: string;
        source_test_id: string;
        source_test_title: string;
        question_start_index: number;
        question_end_index: number;
        num_questions: number;
    }>;
}

export async function mergeTests(request: MergeTestsRequest): Promise<MergeTestsResponse> {
    try {
        logger.info('🔗 Merging tests:', request.source_test_ids.length, 'tests');

        const headers = await getAuthHeaders();
        const response = await fetch(`${API_BASE_URL}/api/v1/tests/merge`, {
            method: 'POST',
            headers,
            body: JSON.stringify(request),
        });

        const result = await handleResponse<MergeTestsResponse>(response);
        logger.info('✅ Tests merged successfully:', result.test_id);
        return result;
    } catch (error) {
        logger.error('❌ Failed to merge tests:', error);
        throw error;
    }
}

